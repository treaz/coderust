package com.horiaconstantin.coderust.trees;

import java.util.HashMap;
import java.util.List;
import java.util.Map;

public class LevelOrderTraversal {

	public static Map<Integer, List<Integer>> levelOrderTraversalAsList(BinaryTreeNode root) {
		if (root == null) {
			return new HashMap<>();
		}
		Map<Integer, List<Integer>> nodeValues = new HashMap<>();
		return nodeValues;
	}

}
